function applyTitles() {
  let totalFiles = 0;
  let totalChapters = 0;
  let mismatches = [];
  for (const [book, titles] of Object.entries(TITLES)) {
    const filePath = path.join(DATA_DIR, `${book}.json`);
    if (!fs.existsSync(filePath)) {
      console.warn(`[skip] ${book}: file not found`);
      continue;
    }
    const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
    if (!Array.isArray(data.chapters)) {
      console.warn(`[skip] ${book}: no chapters array`);
      continue;
    }
    if (data.chapters.length !== titles.length) {
      mismatches.push(
        `${book}: chapters=${data.chapters.length}, titles=${titles.length}`,
      );
    }
    const n = Math.min(data.chapters.length, titles.length);
    for (let i = 0; i < n; i += 1) {
      data.chapters[i].title = titles[i];
    }
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + "\n");
    totalFiles += 1;
    totalChapters += n;
    console.log(`[ok] ${book}: ${n} chapters titled`);
  }
  console.log(`\nDone. Files: ${totalFiles}, chapters: ${totalChapters}`);
  if (mismatches.length) {
    console.log("\nMismatches:");
    mismatches.forEach((m) => console.log("  " + m));
  }
}

applyTitles();
