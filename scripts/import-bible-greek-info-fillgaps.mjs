function applyInfoToTokens(tokens, infos) {
  if (!infos) return 0;
  let attached = 0;
  const remaining = new Map();
  for (const [k, v] of Object.entries(infos))
    remaining.set(stripDiacritics(k), v);
  for (const tok of tokens) {
    if (tok.info) continue;
    if (!tok.w) continue;
    const k = stripDiacritics(tok.w);
    if (remaining.has(k)) {
      tok.info = remaining.get(k);
      remaining.delete(k);
      attached += 1;
    }
  }
  if (remaining.size > 0) {
    console.warn(
      `   ⚠️  매칭 안 됨: ${Array.from(remaining.keys()).join(", ")}`,
    );
  }
  return attached;
}

function main() {
  const data = JSON.parse(fs.readFileSync(romansPath, "utf8"));
  let totalAttached = 0;
  for (const [chStr, chInfos] of Object.entries(INFOS)) {
    const ch = parseInt(chStr, 10);
    const chapter = data.chapters.find((c) => c.chapter === ch);
    if (!chapter) {
      console.warn(`⚠️  ${ch}장 없음`);
      continue;
    }
    let chAttached = 0;
    for (const [nStr, infos] of Object.entries(chInfos)) {
      const n = parseInt(nStr, 10);
      const tokenVerse = chapter.verses.greekTokens.find((tv) => tv.n === n);
      if (!tokenVerse) {
        console.warn(`⚠️  ${ch}:${n} 토큰 없음`);
        continue;
      }
      const attached = applyInfoToTokens(tokenVerse.tokens, infos);
      console.log(`   ${ch}:${n} — ${attached}개 부착`);
      chAttached += attached;
    }
    console.log(`✅ ${ch}장 — 총 ${chAttached}개 부착`);
    totalAttached += chAttached;
  }
  fs.writeFileSync(romansPath, JSON.stringify(data, null, 2) + "\n", "utf8");
  console.log(`\n✅ ${romansPath} 저장 완료 — 총 ${totalAttached}개 부착`);
}

main();
