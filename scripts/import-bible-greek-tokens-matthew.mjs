// 헬라어 글자(악센트·기식 포함) 범위. \p{Script=Greek} 와 결합 표시(Combining Marks).
const GREEK_LETTER = /[\p{Script=Greek}\u0300-\u036f]/u;

// 한 단어/구두점 토큰으로 끊기.
//   - 연속 헬라 글자 = 하나의 단어
//   - 그 외(공백 제외) = 1글자 구두점
function tokenizeGreek(verseText) {
  const out = [];
  let buf = "";
  for (const ch of verseText) {
    if (GREEK_LETTER.test(ch)) {
      buf += ch;
    } else {
      if (buf) {
        out.push(buf);
        buf = "";
      }
      if (/\s/.test(ch)) continue;
      out.push(ch); // 구두점 1글자
    }
  }
  if (buf) out.push(buf);
  return out;
}

