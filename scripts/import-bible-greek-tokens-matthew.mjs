// 강세·기식 제거 (불필요한 정규화는 피하되, fallback 매칭용).
function stripDiacritics(s) {
  return s.normalize("NFD").replace(/[\u0300-\u036f]/g, "").normalize("NFC");
}

function lookupPron(w) {
  if (PRON[w]) return PRON[w];
  const stripped = stripDiacritics(w);
  for (const k of Object.keys(PRON)) {
    if (stripDiacritics(k) === stripped) return PRON[k];
  }
  return null;
}

function lookupInfo(n, w) {
  const key = `${n}:${w}`;
  if (INFO[key]) return INFO[key];
  const stripped = stripDiacritics(w);
  for (const k of Object.keys(INFO)) {
    if (!k.startsWith(`${n}:`)) continue;
    if (stripDiacritics(k.slice(`${n}:`.length)) === stripped) return INFO[k];
  }
  return null;
}

function buildTokens(verseN, verseText) {
  const rawTokens = tokenizeGreek(verseText);
  const out = [];
  const missing = [];
  let infoPlacedFor = new Set(); // 한 절 안에서 같은 단어 중복 info 방지
  for (const raw of rawTokens) {
    if (GREEK_LETTER.test(raw)) {
      // 단어
      const pron = lookupPron(raw);
      if (!pron) missing.push(raw);
      const stripped = stripDiacritics(raw);
      let info = null;
      if (!infoPlacedFor.has(stripped)) {
        info = lookupInfo(verseN, raw);
        if (info) infoPlacedFor.add(stripped);
      }
      const tok = { w: raw, p: pron ?? "" };
      if (info) tok.info = info;
      out.push(tok);
    } else {
      // 구두점
      out.push({ w: raw, p: "" });
    }
  }
  return { tokens: out, missing };
}

function main() {
  const raw = fs.readFileSync(mattPath, "utf8");
  const data = JSON.parse(raw);
  const ch1 = data.chapters.find((c) => c.chapter === 1);
  if (!ch1) throw new Error("chapter 1 not found");
  const greekVerses = ch1.verses.greek ?? [];
  if (greekVerses.length === 0) throw new Error("no greek verses on chapter 1");

  const greekTokens = [];
  const allMissing = [];
  for (const v of greekVerses) {
    const { tokens, missing } = buildTokens(v.n, v.t);
    greekTokens.push({ n: v.n, tokens });
    for (const m of missing) allMissing.push(`v${v.n}: ${m}`);
  }

  ch1.verses.greekTokens = greekTokens;

  // 절 수 일치 검증.
  const krvN = ch1.verses.krv?.length ?? 0;
  const greekKrN = ch1.verses.greekKr?.length ?? 0;
  const greekN = ch1.verses.greek?.length ?? 0;
  const greekTokensN = ch1.verses.greekTokens.length;
  console.log(
    `1장 절 수 — krv:${krvN} greek:${greekN} greekKr:${greekKrN} greekTokens:${greekTokensN}`,
  );
  if (
    !(krvN === greekN && greekN === greekKrN && greekKrN === greekTokensN)
  ) {
    console.warn("⚠️  절 수가 일치하지 않습니다.");
  }

  // 토큰 통계.
  const totalTokens = greekTokens.reduce((s, v) => s + v.tokens.length, 0);
  const withPron = greekTokens.reduce(
    (s, v) => s + v.tokens.filter((t) => t.p).length,
    0,
  );
  const withInfo = greekTokens.reduce(
    (s, v) => s + v.tokens.filter((t) => t.info).length,
    0,
  );
  console.log(
    `토큰 ${totalTokens}개 — 발음 있음:${withPron} / 정보 있음:${withInfo}`,
  );

  // 누락 단어 경고.
  if (allMissing.length > 0) {
    const uniq = Array.from(new Set(allMissing));
    console.warn(`⚠️  발음 누락 ${uniq.length}개:`);
    for (const m of uniq.slice(0, 50)) console.warn(`   ${m}`);
    if (uniq.length > 50) console.warn(`   ... +${uniq.length - 50}`);
  }

  fs.writeFileSync(mattPath, JSON.stringify(data, null, 2) + "\n", "utf8");
  console.log(`✅ ${mattPath} 갱신 완료`);
}

main();
