// 동작:
//   1) matthew.json 을 읽고 1장의 verses.greek 배열을 가져온다.
//   2) 각 절을 단어/구두점 단위로 토큰화한다.
//        - 단어: 헬라어 글자/강세 기호로 구성
//        - 구두점: , . ; · — ʼ ’ ' " 등은 별도 토큰(p 는 "")
//   3) PRON 테이블에서 단어별 한글 발음을 찾는다. 누락된 단어는 콘솔에 경고.
//   4) INFO 테이블에서 그 절(verseN) + 단어(또는 표현)를 찾으면 info 를 단다.
//   5) chapter.verses.greekTokens 에 배열로 저장.
//
// 발음 규칙(일관성):
//   α=아, ε=에, η=에, ι=이, ο=오, ω=오, υ=위(어말 -υ 는 ‘우’)
//   αι=아이, ει=에이, οι=오이, αυ=아우, ευ=에우, ου=우
//   복합어/관행 표기는 PRON 에 직접 정의해 우선 적용.
