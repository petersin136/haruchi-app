function stripKey(s) {
  return stripDiacritics(s);
}

function applyInfoToTokens(tokens, infos) {
  if (!infos) return 0;
  let attached = 0;
  const remaining = new Map();
  for (const [k, v] of Object.entries(infos)) remaining.set(stripKey(k), v);
  for (const tok of tokens) {
    if (tok.info) continue;
    if (!tok.w) continue;
    const k = stripKey(tok.w);
    if (remaining.has(k)) {
      tok.info = remaining.get(k);
      remaining.delete(k);
      attached += 1;
    }
  }
  if (remaining.size > 0) {
    console.warn(
      `   ⚠️  matched 안된 단어 ${remaining.size}개: ${Array.from(remaining.keys()).join(", ")}`,
    );
  }
  return attached;
}

function main() {
  const raw = fs.readFileSync(romansPath, "utf8");
  const data = JSON.parse(raw);

  for (const [chStr, chContent] of Object.entries(CONTENT)) {
    const ch = parseInt(chStr, 10);
    const chapter = data.chapters.find((c) => c.chapter === ch);
    if (!chapter) {
      console.warn(`⚠️  로마서 ${ch}장 못 찾음`);
      continue;
    }
    const krArr = [];
    const wordsArr = [];
    let infoAttached = 0;
    for (const krvVerse of chapter.verses.krv) {
      const v = chContent[krvVerse.n];
      if (!v) {
        console.warn(`⚠️  ${ch}장 ${krvVerse.n}절 — CONTENT 누락`);
        krArr.push({ n: krvVerse.n, t: "" });
        wordsArr.push({ n: krvVerse.n, t: "" });
        continue;
      }
      krArr.push({ n: krvVerse.n, t: v.kr ?? "" });
      wordsArr.push({ n: krvVerse.n, t: v.words ?? "" });
      const tokenVerse = (chapter.verses.greekTokens ?? []).find(
        (t) => t.n === krvVerse.n,
      );
      if (tokenVerse) {
        infoAttached += applyInfoToTokens(tokenVerse.tokens, v.infos);
      }
    }
    chapter.verses.greekKr = krArr;
    chapter.verses.greekWords = wordsArr;
    console.log(
      `✅ ${ch}장 — greekKr ${krArr.length}절 / greekWords ${wordsArr.length}절 / 단어 info ${infoAttached}개 부착`,
    );
  }

  fs.writeFileSync(romansPath, JSON.stringify(data, null, 2) + "\n", "utf8");
  console.log(`✅ ${romansPath} 저장 완료`);
}

main();
