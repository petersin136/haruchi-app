function main() {
  const raw = fs.readFileSync(bookPath, "utf8");
  const data = JSON.parse(raw);
  let totalAttached = 0;
  let totalVerses = 0;
  for (const ch of data.chapters) {
    const chCnt = CONTENT[ch.chapter];
    if (!chCnt) continue;
    const greekKrArr = [];
    const greekWordsArr = [];
    for (const verse of ch.verses.krv) {
      const vc = chCnt[verse.n];
      if (vc) {
        greekKrArr.push({ n: verse.n, t: vc.kr });
        greekWordsArr.push({ n: verse.n, t: vc.words });
        totalVerses += 1;
      } else {
        greekKrArr.push({ n: verse.n, t: "" });
        greekWordsArr.push({ n: verse.n, t: "" });
      }
    }
    ch.verses.greekKr = greekKrArr;
    ch.verses.greekWords = greekWordsArr;
    for (const tokensEntry of ch.verses.greekTokens ?? []) {
      const vc = chCnt[tokensEntry.n];
      if (!vc?.infos) continue;
      console.log(`  ${ch.chapter}장 ${tokensEntry.n}절`);
      totalAttached += applyInfoToTokens(tokensEntry.tokens, vc.infos);
    }
  }
  fs.writeFileSync(bookPath, JSON.stringify(data, null, 2) + "\n", "utf8");
  console.log(`\n✅ ${bookPath} 갱신 완료`);
  console.log(`   콘텐츠 작성 절 ${totalVerses}개, info 부착 토큰 ${totalAttached}개`);
}

main();
