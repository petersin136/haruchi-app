function main() {
  const data = JSON.parse(fs.readFileSync(ephesiansPath, "utf8"));
  let totalAttached = 0;
  for (const [chStr, chInfos] of Object.entries(INFOS)) {
    const ch = parseInt(chStr, 10);
    const chapter = data.chapters.find((c) => c.chapter === ch);
    if (!chapter) {
      console.warn(`⚠️  ${ch}장 없음`);
      continue;
    }
    let chAttached = 0;
    for (const [nStr, infos] of Object.entries(chInfos)) {
      const n = parseInt(nStr, 10);
      const tokenVerse = chapter.verses.greekTokens.find((tv) => tv.n === n);
      if (!tokenVerse) {
        console.warn(`⚠️  ${ch}:${n} 토큰 없음`);
        continue;
      }
      const attached = applyInfoToTokens(tokenVerse.tokens, infos);
      console.log(`   ${ch}:${n} — ${attached}개 부착`);
      chAttached += attached;
    }
    console.log(`✅ ${ch}장 — 총 ${chAttached}개 부착`);
    totalAttached += chAttached;
  }
  fs.writeFileSync(ephesiansPath, JSON.stringify(data, null, 2) + "\n", "utf8");
  console.log(`\n✅ 저장 완료 — 총 ${totalAttached}개 부착`);
}

main();
