function main() {
  const data = JSON.parse(fs.readFileSync(romansPath, "utf8"));
  const chapter = data.chapters.find((c) => c.chapter === 16);
  if (!chapter) {
    console.error("16장 없음");
    process.exit(1);
  }

  for (const [nStr, content] of Object.entries(DATA)) {
    const n = parseInt(nStr, 10);

    // greek 본문 채우기
    const greekVerse = chapter.verses.greek.find((v) => v.n === n);
    if (greekVerse) {
      greekVerse.t = content.greek;
      console.log(`✅ 16:${n} greek 본문 채움`);
    }

    // 토큰 채우기
    const tokenVerse = chapter.verses.greekTokens.find((v) => v.n === n);
    if (tokenVerse) {
      tokenVerse.tokens = content.tokens;
      const infoCount = content.tokens.filter((t) => t.info).length;
      console.log(
        `✅ 16:${n} 토큰 ${content.tokens.length}개 / info ${infoCount}개 부착`,
      );
    }
  }

  fs.writeFileSync(romansPath, JSON.stringify(data, null, 2) + "\n", "utf8");
  console.log(`\n✅ ${romansPath} 저장 완료`);
}

main();
