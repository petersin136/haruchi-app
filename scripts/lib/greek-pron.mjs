// 자동 음역(transliteration) — 헬라어 글자 → 한글 음절 합성.
//
// 에라스무스식 발음을 한국어로 표기하는 가장 단순한 룰:
//   1) 강세/기식/구두점 제거
//   2) 디프통 우선 매칭 (αι=ㅐ→아이, ει=에이, οι=오이, αυ=아우, ευ=에우, ου=우)
//   3) 자음+모음 페어를 한글 음절로 합성 (초성+중성)
//   4) 모음 없는 자음(어말/연속) 은 별도 음절로 — 단순 라틴 자음 발음으로
//      (γ=그, β=브, etc.)
//   5) 모음만 있는 음절 — 'ㅇ' 초성과 결합
//
// 한계: 격·시제에 따른 미세한 차이는 못 잡고, ψ·ξ 같은 자음 결합은
// 2음절로 분리됨(예: ψ → 프스). 학습용 보조 표기로 사용.
// =============================================================================

const INITIALS = [
  "ㄱ","ㄲ","ㄴ","ㄷ","ㄸ","ㄹ","ㅁ","ㅂ","ㅃ","ㅅ","ㅆ","ㅇ","ㅈ","ㅉ","ㅊ","ㅋ","ㅌ","ㅍ","ㅎ",
];
const MEDIALS = [
  "ㅏ","ㅐ","ㅑ","ㅒ","ㅓ","ㅔ","ㅕ","ㅖ","ㅗ","ㅘ","ㅙ","ㅚ","ㅛ","ㅜ","ㅝ","ㅞ","ㅟ","ㅠ","ㅡ","ㅢ","ㅣ",
];
function syllable(initial, medial) {
  const ii = INITIALS.indexOf(initial);
  const mi = MEDIALS.indexOf(medial);
  if (ii < 0 || mi < 0) return initial + medial;
  return String.fromCharCode(0xAC00 + (ii * 21 + mi) * 28);
}

