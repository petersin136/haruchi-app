// ── 키 끝에 '_' / '_2' 같은 임시 마커 또는 값이 0 인 placeholder 항목 정리.
//   같은 Strong's 번호를 여러 번 인용해야 할 때 임시로 _, _2 를 붙여
//   주석성으로 적었던 흔적. 모두 제거하고, 같은 키의 마지막 정의만 살린다.
for (const k of Object.keys(TANAKH_LEXICON)) {
  if (TANAKH_LEXICON[k] === 0 || /_(?:\d+)?$/.test(k)) {
    delete TANAKH_LEXICON[k];
  }
}

// ── lemma raw → Strong's 키 정규화 ──────────────────────────────────────────
// OSHB 의 lemma 칸은 "430", "1254 a", "b/7225" 처럼 다양한 형태.
// "/" 로 갈라진 prefix 는 빌더가 처리하고, 본 함수는 단일 형태소를 받아
// "H+숫자" 키로 정규화한다.
export function normalizeStrong(lemmaPart) {
  if (!lemmaPart) return "";
  const trimmed = lemmaPart.trim();
  // 1글자 prefix (b/c/d/k/l/m/s/i/a) 는 그대로 반환 — 빌더가 PREFIX_LEMMA 처리
  if (trimmed.length === 1 && /[a-z]/.test(trimmed)) return trimmed;
  // "1254 a" → 1254 (a/b suffix 무시 — 추후 정밀화 가능)
  const m = trimmed.match(/^(\d+)/);
  if (!m) return "";
  return "H" + m[1];
}

// 한국어 lemma lookup. 없으면 빌더가 Strong's 의 영어 def 로 fallback.
export function lookupTanakh(strongKey) {
  if (!strongKey) return null;
  return TANAKH_LEXICON[strongKey] || null;
}
