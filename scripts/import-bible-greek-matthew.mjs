// 메인
const matt = JSON.parse(fs.readFileSync(mattPath, "utf8"));

let totalMismatch = 0;
let totalAdded = 0;

for (const [chStr, verseMap] of Object.entries(WORDS)) {
  const ch = Number(chStr);
  const chapter = matt.chapters.find((c) => c.chapter === ch);
  if (!chapter) {
    console.error(`!! Chapter ${ch} not found`);
    continue;
  }
  const verseNumbers = Object.keys(verseMap)
    .map(Number)
    .sort((a, b) => a - b);
  const krvCount = chapter.verses.krv.length;
  const greekCount = chapter.verses.greek?.length ?? 0;

  // greekKr / greekWords 배열 재구성
  const krArr = [];
  const wordsArr = [];
  for (const n of verseNumbers) {
    const entry = verseMap[n];
    krArr.push({ n, t: entry.kr });
    wordsArr.push({ n, t: entry.words });
  }
  chapter.verses.greekKr = krArr;
  chapter.verses.greekWords = wordsArr;
  // 기존에 들어있던 한 단어씩 풀이 형식은 위에서 통째로 갈아엎혔다.

  const krCount = krArr.length;
  const wCount = wordsArr.length;
  const ok =
    krvCount === greekCount &&
    greekCount === krCount &&
    krCount === wCount;
  console.log(
    `Ch ${ch}: krv=${krvCount} greek=${greekCount} greekKr=${krCount} greekWords=${wCount} ${
      ok ? "✓" : "✗"
    }`,
  );
  if (!ok) totalMismatch += 1;
  totalAdded += krCount;
}

fs.writeFileSync(mattPath, JSON.stringify(matt, null, 2) + "\n");
console.log(`\n갱신 절수(greekKr+greekWords 동수): ${totalAdded}`);
if (totalMismatch > 0) {
  console.error(`불일치 장 ${totalMismatch} 개 — 위 로그 확인`);
  process.exit(1);
}
